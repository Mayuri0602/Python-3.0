# Bitwise Operators:-
# | -> or
# & -> and
# ^ -> XOR
# ~ -> not
# >> -> right shift
# << -> left shift

a = 35
print(bin(a))

# 0r
a = 21
b = 17
print(bin(a))
print(bin(b))
print(a|b)

# and
c = 23
d = 13
print(bin(c))
print(bin(d))
print(c&d)

# not
print(~7)

# xor
print(21^35)
